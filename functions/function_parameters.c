/*
Function Parameters
Functions can have parameters, which they can use in their
code.
The parameters are defined in the parentheses, and can be
used like variables in the function.

For example, let's add a string name parameter to
our greeting() function.
*/

void greeting(char name[]){
    printf("Hello, %s!", name);
}
/*
The function above takes a string called name as its
parameter, which is used in the function's output.
*/