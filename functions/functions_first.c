/*
Functions
A function is a block of code designed to perform a particular
task.
For example, your program can have functions like login(), draw(),
convert(), calculate(), etc.

We have seen some functions in the previous lessons, for
example, the main() function, the printf() function, the
scanf() function.

You can define your own functions to perform your desired
tasks.
Here is an example.
*/

void greeting(){
    printf("Hello! \n");
    printf("I am an example function.");
}
/*
void means that this function does not have a return
value. You will learn more about return values later 
in this module.

greeting is the name of the function, and is followed
by parenthese().

The body of the function is enclosed in curly brackets{}
*/